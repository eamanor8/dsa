/*
    A binary tree is said to be complete if an array representation of the binary tree
    is such that no empty spaces are left in the array.

    Max Heap: Every node should have a value greater than or equal to all it's descendents.
    Here, duplicates are allowed.

    Min Heap: Every node should have a value less than or equal to all it's descendents.

    A Heap is simply a complete binary tree that satisfies either the max heap or min heap conditions.
*/